//require the colors module
var color = require('colors');

//print out a rainbow string
console.log(color.rainbow("An annoying rainbow sentence!"));

//print out a zebra string
console.log(color.zebra("black and white...classy!"));

//print out a red,white and blue string
console.log(color.america('MERICA!'));

//random character trap
console.log(color.trap("It's a trap!"));

//random style applied to each character of the string
console.log(color.random("this just hurts"));
